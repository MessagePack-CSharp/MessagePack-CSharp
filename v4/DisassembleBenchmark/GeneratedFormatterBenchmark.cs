using BenchmarkDotNet.Attributes;
using BenchmarkDotNet.Configs;
using System.Buffers;

// The source-generated BenchPerson formatter (module-initializer-registered, resolved by
// UltraMessagePack.MessagePackSerializer.Default) vs the hand-written BenchPersonFormatter
// (explicit factory chain) vs MessagePack-CSharp vs Nerdbank.MessagePack. The generated
// output follows the measured house shape (batched serialize reservation, loop+switch
// deserialize), so Generated == Handwritten within noise is the acceptance target: the
// generator should cost nothing over the hand-tuned formatter.
//
// NOTE: this benchmark class must NOT register the hand-written factory into
// DynamicFormatterFactory.Instance for BenchPerson (SerializerBenchmark does that in its
// own process) — Default therefore resolves the GENERATED formatter here.
//
// MEASURED (Zen 5, ShortRun, single BenchPerson{int,string,double}, ns/op):
//   Serialize:   Generated 19.4 | Handwritten 20.0 | MessagePack-CSharp 32.6 | Nerdbank 66.5
//   Deserialize: Generated 28.1 | Handwritten 28.3 | MessagePack-CSharp 52.1 | Nerdbank 84.8
// VERDICT: acceptance target hit — Generated == Handwritten within noise (the emitter
// reproduces the measured house shape exactly: one batched reservation on serialize,
// loop+switch + utf8 key match on deserialize, Initialize-resolved formatter fields),
// 1.7x/1.9x over MessagePack-CSharp and 3.4x/3.0x over Nerdbank on this poco. Bytes are
// oracle-identical (verified in Setup).
[GroupBenchmarksBy(BenchmarkLogicalGroupRule.ByCategory)]
[CategoriesColumn]
public class GeneratedFormatterBenchmark
{
    BenchPerson person = default!;
    byte[] payload = default!;
    UltraMessagePack.MessagePackSerializer handwritten = default!;
    readonly Nerdbank.MessagePack.MessagePackSerializer nb = new();

    [GlobalSetup]
    public void Setup()
    {
        person = new BenchPerson { Id = 12345, Name = "山岡士郎", Score = 98.5 };

        handwritten = new UltraMessagePack.MessagePackSerializer(
            new BenchPersonFormatterFactory(),
            UltraMessagePack.PrimitiveFormatterFactory.Instance,
            UltraMessagePack.GenericFormatterFactory.Instance);

        payload = MessagePack.MessagePackSerializer.Serialize(person);
        if (!UltraMessagePack.MessagePackSerializer.Default.Serialize(person).AsSpan().SequenceEqual(payload)) throw new InvalidOperationException("verify failed: generated bytes vs oracle");
        if (!handwritten.Serialize(person).AsSpan().SequenceEqual(payload)) throw new InvalidOperationException("verify failed: handwritten bytes vs oracle");
        var back = UltraMessagePack.MessagePackSerializer.Default.Deserialize<BenchPerson>(payload)!;
        if (back.Id != person.Id || back.Name != person.Name || back.Score != person.Score) throw new InvalidOperationException("verify failed: generated roundtrip");
    }

    [BenchmarkCategory("Serialize"), Benchmark(Baseline = true)]
    public byte[] SerializeGenerated() => UltraMessagePack.MessagePackSerializer.Default.Serialize(person);

    [BenchmarkCategory("Serialize"), Benchmark]
    public byte[] SerializeHandwritten() => handwritten.Serialize(person);

    [BenchmarkCategory("Serialize"), Benchmark]
    public byte[] SerializeMpcs() => MessagePack.MessagePackSerializer.Serialize(person);

    [BenchmarkCategory("Serialize"), Benchmark]
    public byte[] SerializeNerdbank() => nb.Serialize(person);

    [BenchmarkCategory("Deserialize"), Benchmark(Baseline = true)]
    public BenchPerson DeserializeGenerated() => UltraMessagePack.MessagePackSerializer.Default.Deserialize<BenchPerson>(payload)!;

    [BenchmarkCategory("Deserialize"), Benchmark]
    public BenchPerson DeserializeHandwritten() => handwritten.Deserialize<BenchPerson>(payload)!;

    [BenchmarkCategory("Deserialize"), Benchmark]
    public BenchPerson DeserializeMpcs() => MessagePack.MessagePackSerializer.Deserialize<BenchPerson>(payload)!;

    [BenchmarkCategory("Deserialize"), Benchmark]
    public BenchPerson DeserializeNerdbank() => nb.Deserialize<BenchPerson>(new ReadOnlySequence<byte>(payload))!;
}
